  <h2 class="centralizar1"><em>Ingredientes</em></h2>
  <br>
        <ul class="centralizar">
      <li>3 cenouras médias (250g)</li>
      <li>4 ovos</li>
      <li>meia xícara (chá) de óleo</li>
      <li>2 e meia xícaras (chá) de farinha de trigo</li>
      <li>2 xícaras (chá) de açúcar</li>
      <li>1 colher (sopa) de fermento em    pó</li>
   </ul>
     
  
      <section>
       
      <h2 class="centralizar1"><em>Modo De Preparo</em></h2>
        
      <div > 
      <ol class="centralizar"><strong>
            <li>Em um liquidificador, bata as cenouras, os ovos e o óleo.</li>

            <li>Despeje a mistura em um recipiente e misture o açúcar e a farinha de trigo peneirada com o fermento.</li>

            <li>Coloque em uma fôrma retangular (20 x 30 cm) untada, e leve ao forno médio (180°C), preaquecido, por 40 minutos.</li>

            <li>Enquanto isso, prepare a cobertura de brigadeiro.</li>
      </ol>
    </div>
